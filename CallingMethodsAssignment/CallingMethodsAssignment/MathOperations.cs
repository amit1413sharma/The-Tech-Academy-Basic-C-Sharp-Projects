using System; // Provides basic system functionality

namespace CallingMethodsAssignment
{
    // This class contains methods that perform math operations
    class MathOperations
    {
        // This method takes an integer, adds 10 to it, and returns the result
        public int AddTen(int number)
        {
            return number + 10;
        }

        // This method takes an integer, multiplies it by 2, and returns the result
        public int MultiplyByTwo(int number)
        {
            return number * 2;
        }

        // This method takes an integer, subtracts 5 from it, and returns the result
        public int SubtractFive(int number)
        {
            return number - 5;
        }
    }
}
